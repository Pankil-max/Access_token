public class MathOperators{
    public static void main(String[] args){
        //Arithmetic operators
        int a=5;
        int b=10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a%b);
        //Unary Operator
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(--b);
        System.out.println(b--);
        System.out.println(b);
        System.out.println(a+b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        int c=(a>b)?a:b;
        System.out.println(c);
        
    
    }    
}